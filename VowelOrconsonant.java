public class VowelOrconsonant {
public static void main(String[] args) {

char ch = 'a';

switch (ch) {
            case 'a':
	
            case 'b':

            case 'c':

            case 'd':

            case 'e':
                System.out.println(ch + " is vowel");
                break;
            default:
                System.out.println(ch + " is consonant");
}
}
}